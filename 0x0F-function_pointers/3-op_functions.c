/**
 * op_add - Returns sum of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Returns difference of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Returns product of two integers
 * @a: First integer
 * @b: Second integer
 *
 * Return: Product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Returns quotient
 * @a: FIrst integer
 * @b: Second integer
 *
 * Return: Quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Returns remainder
 * @a: First integer
 * @b: Second integer
 *
 * Return: Remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
